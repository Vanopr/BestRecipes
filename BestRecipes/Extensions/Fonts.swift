import UIKit

extension UIFont {

  static func poppinsRegular(size: CGFloat) -> UIFont? {
    return UIFont(name: "Poppins-Regular", size: size)
  }

  static func poppinsSemiBold(size: CGFloat) -> UIFont? {
    return UIFont(name: "Poppins-SemiBold", size: size)
  }
}
